package org.example;

public class Plano {

    private String nome;
    private String descricao;
    private double velocidade;
    private double preco;

    public Plano(String nome, String descricao, double velocidade, double preco) {
        this.nome = nome;
        this.descricao = descricao;
        this.velocidade = velocidade;
        this.preco = preco;
    }
}
